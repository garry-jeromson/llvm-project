#ifndef LLVM_LIB_TARGET_W65816_MCTARGETDESC_W65816INSTPRINTER_H
#define LLVM_LIB_TARGET_W65816_MCTARGETDESC_W65816INSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class W65816InstPrinter : public MCInstPrinter {
public:
  W65816InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                    const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &OS) override;

  void printRegName(raw_ostream &OS, MCRegister Reg) override;

  // Autogenerated by tblgen
  std::pair<const char *, uint64_t> getMnemonic(const MCInst &MI) const override;
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  static const char *getRegisterName(MCRegister Reg);

private:
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
  void printAddrModeMemSrc(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
  void printLongAddr(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
};

} // namespace llvm

#endif // LLVM_LIB_TARGET_W65816_MCTARGETDESC_W65816INSTPRINTER_H
